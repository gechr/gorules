//go:build ignore

// Command clog writes clog.go: one ruleguard rule per clog builder-chain
// length. ruleguard parses its rule file as data and only
// accepts literal func declarations with literal Match/Suggest strings (no
// loops, no fmt.Sprintf), so the rules cannot be generated at ruleguard load
// time - they are generated here instead and committed.
//
// The rule: a clog builder chain may sit on one line only when it has a
// single field call (clog.With().Str("k", v).Logger()); two or more field
// calls must wrap onto one call per line, regardless of width. These rules -
// not golines - own that layout: golines only breaks chains wider than its
// limit, so without them a short multi-field chain would stay inline.
//
// minChain is the smallest chain that must wrap (2); maxChain is the largest
// we emit a rule for. It comfortably exceeds any chain that could still fit
// on one line. Bump maxChain and run `make gen` if a longer chain ever
// appears.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
)

const (
	minChain = 2
	maxChain = 8
)

var ordinals = []string{
	"",
	"One",
	"Two",
	"Three",
	"Four",
	"Five",
	"Six",
	"Seven",
	"Eight",
	"Nine",
	"Ten",
}

func main() {
	const bt = "`"

	var b bytes.Buffer
	b.WriteString("// Code generated by gen/clog.go; DO NOT EDIT.\n\n")
	b.WriteString("package gorules\n\n")
	b.WriteString(`import "github.com/quasilyte/go-ruleguard/dsl"` + "\n")

	for n := minChain; n <= maxChain; n++ {
		var match, suggest bytes.Buffer
		for i := 1; i <= n; i++ {
			fmt.Fprintf(&match, ".$m%d($*a%d)", i, i)
			fmt.Fprintf(&suggest, "$m%d($a%d).\n", i, i)
		}
		fmt.Fprintf(&b, "\nfunc ClogChain%s(m dsl.Matcher) {\n", ordinals[n])
		fmt.Fprintf(&b, "\tm.Match(%s$log.With()%s.Logger()%s).\n", bt, match.String(), bt)
		fmt.Fprintf(&b, "\t\tSuggest(%q)\n", "$log.With().\n"+suggest.String()+"Logger()")
		b.WriteString("}\n")
	}

	src, err := format.Source(b.Bytes())
	if err != nil {
		log.Fatalf("format generated source: %v", err)
	}
	if err := os.WriteFile("clog.go", src, 0o600); err != nil {
		log.Fatalf("write clog.go: %v", err)
	}
}
